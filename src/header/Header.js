function font_gradient_in(){
  var font_gradient = function(callback) { this.init(function(){callback();}); },
      f_g = font_gradient.prototype,
      elements = document.querySelectorAll("." + styles.animation__gradient);
  var max = 0;
  for(var k=0; k < elements.length; k++){
    var count = elements[k].children.length;
    if( max < count){
      max = count
    }
  }

  f_g.init = function(){
    f_g.initColors(0, 100);
  }
  f_g.initColors = function(i, number){
    if(elements[0].children[i] !== undefined){
      elements[0].children[i].dataset.grad = number;
    }
    setTimeout(function(){
      if(elements[1] !== undefined && elements[1].children[i] !== undefined){
          elements[1].children[i].dataset.grad = number;
        }
      if(elements[2] !== undefined && elements[2].children[i] !== undefined){
          elements[2].children[i].dataset.grad = number;
        }
      if(elements[2] !== undefined && elements[2].children[i] !== undefined){
          elements[2].children[i].dataset.grad = number;
        }
      if(elements[3] !== undefined && elements[3].children[i] !== undefined){
          elements[3].children[i].dataset.grad = number;
        }
      if(elements[4] !== undefined && elements[4].children[i] !== undefined){
          elements[4].children[i].dataset.grad = number;
        }
      if(elements[5] !== undefined && elements[5].children[i] !== undefined){
          elements[5].children[i].dataset.grad = number;
        }
    },300)
    f_g.start(i, number);
  }
  f_g.start = function(i, number){
    i = i + 1;
    if(i === max){
      f_g.animation(number)
    }else{
      setTimeout(function(){
        f_g.initColors(i, number)
      }, 400);
    }
  }
  f_g.animation = function(number){
    switch(number){
      case 0:
        f_g.initColors(0, 100);
        break;
      case 100:
        f_g.initColors(0, 0);
        break;
      default:
        break;
    }
  }
  window.font_gradient = font_gradient;
  font_gradient = new font_gradient();
}

function font_trans(){

  var font_trans = function(callback) { this.init(function(){callback();}); },
      f_t = font_trans.prototype,
      elements = document.querySelectorAll("."+styles.animation__block),
      count = elements.length;


  f_t.init = function(){
    f_t._start_new()
  }
  f_t._start = function(index){

    index = index + 1;
    f_t._animation(index)

  }
  f_t._start_new = function(){
    f_t._animation(0);
  }
  f_t._animation = function(index){

    var index2 = 0;

    if(index >= (count - 1)){
      index2 = 0;
    }else{
      index2 = index + 1;
    }

    // start line
    f_t._animation_line(index);

    // start line 2
    setTimeout(function(){
      f_t._animation_line(index2);
    }, 3800)

    // reload index2
    setTimeout(function(){
      f_t._help(index2)
    }, 7600)
  }
  f_t._help = function(index){

    if( index >= (count - 1)){
      f_t._start_new()
    }else{
      f_t._start(index)
    }
  }
  f_t._animation_line = function(index){
    elements[index].classList.remove(styles.active__none);
    setTimeout(function(){
      elements[index].classList.add(styles.active__t);
    }, 100)
    setTimeout(function(){
      elements[index].classList.add(styles.active__b);
      // _animation(index2);
      setTimeout(function(){
        elements[index].classList.remove(styles.active__t);

        setTimeout(function(){
          elements[index].classList.add(styles.active__none);
          elements[index].classList.remove(styles.active__b);
          return true;
        }, 3000)
      }, 1000)
    }, 3000)
  }
  window.font_trans = font_trans;
  font_trans = new font_trans();
}

